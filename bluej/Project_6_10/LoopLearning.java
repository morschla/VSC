

public class LoopLearning
{
    public static void main(String[] args){
        // 1. Initionalization
        // 2. Test
        // 3. Change
        int i = 0;
        while (i < 10){
            System.out.println(i);
            i++;
        }
        
        // for(intitialization; test; change)
        for(int j; j < 10; j++){
            System.out.println(j);
        }
        
        
        
        
        
        
    }
}
